//1. while의 무한루프
//while(true) {alert('반복실행');}		//조건값이 true니까 반복실행

//2. while [ 조건이 상수이면 반복(고정) ]
//while( 10> 5 ){ alert('실행문');}	//10이 5보다 크니까 반복실행

//3. while [ 조건을 변수로 이용한 반복 ]	//입력에 따른 조건 결과가 바뀌니까 반복 여부 
let 입력 = prompt('입력:')
while(입력 !='exit'){		//while s	//입력된 값이 exit 아니면 아래 코드 실행
	console.log('실행중')
	입력 = prompt('입력') //반복적으로 입력 받기
}//while end // while 종료되는 조건 :  입력된 값이 exit 이면 종료

//4. while [ 순서대로 바복 ]
let i = 0; //반복 순서 번호 변수[ 반복 몇번 되었는지 체크 함수 ]
while( i < 10 ){		 //while s // 종료조건 : i가 10이면[false] 종료
	//i는 0부터 9까지 1씩 증가 반복 = 10회 반복
	i++;	//i변수[반복변수] 1증가
	console.log('실행중2 :' + i);
}

//1. for문 [  ]
for (let j = 0; j<10; j++){ // fors
	//i는 0부터 10미만[9]까지 1씩 증가 하면서 반복 = 10회 반복
	console.log('실행중3 :' + j)
} // for e

//2. 배열과 for문
let 과일상자 = ['사과', '포도', '딸기'] //데이터 3개를 저장하는 배열 선언
	//1. 0부터 마지막 인덱스까지의 반복해서 배열명[인덱스=j] 요ㅗ 호출
for(let j = 0 ; j < 과일상자.length; j++){ //for s
	//j는 0부터 배열의길이[3] 미만까지 1씩 증가 하면서 반복 = 3회 반복(0 1 2)
		//인덱스[0부터 시작] === 길이-1 [1부터 시작]
	console.log(j+"번째요소"+과일상자[j])
}//for e

	//2. of 기준으로 오른쪽에 있는 배열/리스트 내 요소 하나씩왼쪽 변수에 반복 대입
for(let j of 과일상자 ){//for s
	console.log( " 요소:" + j )
}// for e
	
	//3. in 키워드 : 기준으로 오른쪽에 있는 배열/리스트 내 요소 인덱스를 하나씩 왼ㅉ고 변수에 반복 대입
for( let j in 과일상자){
	console.log( "요소의 인덱스 :" + j )
}	